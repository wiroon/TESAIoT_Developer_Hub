/* Icon name lookup table */
static const char *ui_icon_names[UI_ICON_COUNT] = {
    "heart", "star", "flag", "trophy", "skull",
    "arrow_up", "arrow_down", "arrow_left", "arrow_right",
    "check", "cross", "smiley", "car", "boat", "plane", "home"
};

/* Lookup icon index by name. Returns -1 if not found. */
static inline int ui_icon_lookup(const char *name)
{
    for (int i = 0; i < UI_ICON_COUNT; i++) {
        if (strcmp(name, ui_icon_names[i]) == 0) {
            return i;
        }
    }
    return -1;
}

