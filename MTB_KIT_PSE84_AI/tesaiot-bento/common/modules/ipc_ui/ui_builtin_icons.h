/*******************************************************************************
 * File Name: ui_builtin_icons.h
 *
 * Description: Built-in 24x24 monochrome icon bitmaps for ui.Image() widget.
 *              Each icon is 72 bytes (3 bytes per row x 24 rows).
 *              Bit=1 means foreground (user color), Bit=0 means transparent.
 *              MSB = leftmost pixel.
 *
 *              Usage on CM55:
 *                int idx = ui_icon_lookup("heart");
 *                if (idx >= 0) render ui_builtin_icons[idx] to lv_canvas
 *
 *******************************************************************************/

